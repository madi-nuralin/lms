     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $lesson)
    {
	$input = $request->all();
	$lesson = \App\Entities\Lesson::findOrFail($lesson);

	if (isset($input['attendances'])) {
		foreach ($lesson->attendances as $attendance) {
			$attendance->status = false;
			if ( in_array($attendance->id, $input["attendances"]) )
				$attendance->status = true;
			$attendance->save();
		}
	} else {
		foreach ($lesson->attendances as $attendance) {
			$attendance->status = false;
			$attendance->save();
		}
	}

	flash('Attendance information successfully updated');

	return redirect()->back();
    }

